namespace Orvibo
{
    /// <summary>
    ///     Physical state of the socket; i.e. off or on.
    /// </summary>
    public enum SocketState
    {
        /// <summary>
        ///     Unknown.
        /// </summary>
        Unknown,

        /// <summary>
        ///     Socket switched off.
        /// </summary>
        Off,

        /// <summary>
        ///     Socket switched on.
        /// </summary>
        On
    }
}
